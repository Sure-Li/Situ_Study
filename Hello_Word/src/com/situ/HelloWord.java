	public static void main(String[] args) {
		System.out.println("hello word");
		System.out.println("hello word");
		int a = 10;
		int b = 20;
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a % b);
		for (int j = 1; j <= 10; j++) {
			System.out.println(j);
		}
//		双精度浮点型
		double a1 = 2.0;
		double a2 = 1.1;
		double a3 = 2.1;
		System.out.println(a2 - a1);
		System.out.println(a2 - a3);
	}
